#include <bits/stdc++.h>
using namespace std;

const int maxn = 1e5 + 5;
int a[maxn], b[maxn];

int main()
{
    int n, y1;
    scanf("%d%d", &n, &y1);
    for (int i = 1; i <= n; ++i)
        scanf("%d", &a[i]);
    int m, y2;
    scanf("%d%d", &m, &y2);
    for (int i = 1; i <= m; ++i)
        scanf("%d", &b[i]);
    return 0;
}